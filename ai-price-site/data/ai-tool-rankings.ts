export function getCategoryScore(
  item: AiToolRankingItem,
  category: AiToolCategoryKey,
) {
  const baseScore = getAiToolCategoryScore(item.scores, category);
  const fitAdjustment = item.categoryFit?.[category] ?? 0;
  return Math.max(0, Math.min(100, baseScore + fitAdjustment));
}

export function getTopToolsForCategory(category: AiToolCategoryKey, limit = 5) {
  return aiToolRankingItems
    .filter((item) => item.categoryKeys.includes(category))
    .sort((a, b) => {
      const scoreGap = getCategoryScore(b, category) - getCategoryScore(a, category);
      if (scoreGap !== 0) return scoreGap;

      return getWeightedScore(b) - getWeightedScore(a);
    })
    .slice(0, limit);
}

export function getWeightedScore(item: AiToolRankingItem) {
  return getAiToolScoreTotal(item.scores);
}
